# Operadores aritmeticos (arithmetic operators) en Python.
# Version de Python (Python Version): existen desde Python 1.0 (January 1994).

# Python soporta operaciones aritmeticas estandar: suma, resta, multiplicacion,
# division, division entera, modulo y exponenciacion.
# Explicación: estos operadores representan funciones binarias f(a, b) -> resultado.

# Suma (addition, +).
# Explicación: + representa addition; con a = 10 y b = 5, a + b = 15.
a = 10
b = 5
print('a + b =', a + b)  # Salida (output): a + b = 15

# Resta (subtraction, -).
# Explicación: - representa subtraction; a - b = 10 - 5 = 5.
print('a - b =', a - b)  # Salida: a - b = 5

# Multiplicacion (multiplication, *).
# Explicación: * representa multiplication; a * b = 10 * 5 = 50.
print('a * b =', a * b)  # Salida: a * b = 50

# Division (division, /).
# Explicación: / representa division real; a / b = 10 / 5 = 2.0.
print('a / b =', a / b)  # Salida: a / b = 2.0

# Division entera (floor division, //).
# Explicación: // representa floor(a / b); floor(10 / 5) = 2.
print('a // b =', a // b)  # Salida: a // b = 2

# Modulo (modulus, %).
# Explicación: % representa el residuo r en a = b*q + r; 10 = 5*2 + 0.
print('a % b =', a % b)  # Salida: a % b = 0

# Exponenciacion (exponentiation, **).
# Explicación: ** representa potencia; a ** b = a^b = 10^5 = 100000.
print('a ** b =', a ** b)  # Salida: a ** b = 100000

# Nota: los operadores aritmeticos soportan enteros (integers) y floats,
# y siguen reglas de precedencia (operator precedence).
