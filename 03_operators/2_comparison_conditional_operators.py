# Operadores de comparacion y condicionales (comparison/conditional operators) en Python.
# Version de Python (Python Version): existen desde Python 1.0 (January 1994).

# Los operadores de comparacion permiten comparar dos valores y devuelven un booleano (boolean).
# Explicación: una comparacion evalua una relacion R(a, b) y devuelve True o False.

# Igual a (equal to, ==).
# Explicación: == representa igualdad; 5 = 5 es verdadero.
print('5 == 5:', 5 == 5)  # Salida (output): True

# Diferente de (not equal to, !=).
# Explicación: != representa desigualdad; 5 != 3 es verdadero.
print('5 != 3:', 5 != 3)  # Salida: True

# Mayor que (greater than, >).
# Explicación: > representa orden estricto; 5 > 3 es verdadero.
print('5 > 3:', 5 > 3)  # Salida: True

# Menor que (less than, <).
# Explicación: < representa orden estricto; 5 < 3 es falso.
print('5 < 3:', 5 < 3)  # Salida: False

# Mayor o igual que (greater than or equal to, >=).
# Explicación: >= significa mayor que o igual; 5 >= 5 es verdadero por igualdad.
print('5 >= 5:', 5 >= 5)  # Salida: True

# Menor o igual que (less than or equal to, <=).
# Explicación: <= significa menor que o igual; 3 <= 5 es verdadero.
print('3 <= 5:', 3 <= 5)  # Salida: True

# Nota: se usan mucho en sentencias condicionales (conditional statements)
# como if-else y en bucles (loops).
