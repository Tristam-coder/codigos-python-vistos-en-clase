# Operaciones basicas con numeros (numbers) en Python.
# Version de Python (Python Version): Python soporta int y float desde la version 1.0.

# Python soporta enteros (integer, int), numeros de punto flotante (floating-point, float)
# y varias operaciones matematicas.
# Explicación: a y b son operandos; las operaciones producen nuevos valores numericos.

# Suma (addition), resta (subtraction), multiplicacion (multiplication), division (division).
# Explicación: con a = 10 y b = 3: a + b, a - b, a * b, a / b.
a = 10
b = 3
print('a + b =', a + b)  # Salida (output): a + b = 13
print('a - b =', a - b)  # Salida: a - b = 7
print('a * b =', a * b)  # Salida: a * b = 30
print('a / b =', a / b)  # Salida: a / b = 3.3333333333333335

# Division entera (floor division, //) y modulo (modulus, %).
# Explicación: division euclidiana: a = b*q + r, con q = a // b y r = a % b.
print('a // b =', a // b)  # Salida: a // b = 3 (division entera)
print('a % b =', a % b)  # Salida: a % b = 1 (residuo/remainder)

# Exponenciacion (exponentiation).
# Explicación: a ** b = a^b; aqui 10^3 = 1000.
print('a ** b =', a ** b)  # Salida: a ** b = 1000 (10 elevado a 3)

# Nota: las operaciones numericas de Python soportan aritmetica compleja,
# aritmetica de punto flotante (floating-point arithmetic) y enteros grandes.
